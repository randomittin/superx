const COLOR_MAP = { 0: null };
// Colors 1-9 map to palette keys

function renderSprite(agentType) {
  const map = SPRITE_MAPS[agentType];
  const pal = PALETTES[agentType];
  if (!map || !pal) return null;

  const canvas = document.createElement('canvas');
  const size = SPRITE_SIZE * SCALE;
  canvas.width = size;
  canvas.height = size;
  const ctx = canvas.getContext('2d');

  // Draw pixelated gradient background
  const grad = GRADIENTS[agentType];
  if (grad) {
    for (let y = 0; y < SPRITE_SIZE; y++) {
      const t = y / SPRITE_SIZE;
      const r1 = parseInt(grad[0].slice(1,3), 16), g1 = parseInt(grad[0].slice(3,5), 16), b1 = parseInt(grad[0].slice(5,7), 16);
      const r2 = parseInt(grad[1].slice(1,3), 16), g2 = parseInt(grad[1].slice(3,5), 16), b2 = parseInt(grad[1].slice(5,7), 16);
      const r = Math.round(r1 + (r2 - r1) * t);
      const g = Math.round(g1 + (g2 - g1) * t);
      const b = Math.round(b1 + (b2 - b1) * t);
      // Dithered: add pixel dots for NFT look
      for (let x = 0; x < SPRITE_SIZE; x++) {
        const dither = ((x + y) % 3 === 0) ? 15 : 0;
        ctx.fillStyle = `rgb(${Math.min(255, r + dither)},${Math.min(255, g + dither)},${Math.min(255, b + dither)})`;
        ctx.fillRect(x * SCALE, y * SCALE, SCALE, SCALE);
      }
    }
  }

  // Draw sprite on top
  for (let y = 0; y < SPRITE_SIZE; y++) {
    for (let x = 0; x < SPRITE_SIZE; x++) {
      const val = map[y][x];
      if (val === 0) continue;
      const color = pal[val];
      if (!color) continue;
      ctx.fillStyle = color;
      ctx.fillRect(x * SCALE, y * SCALE, SCALE, SCALE);
    }
  }

  return canvas.toDataURL();
}

function generateAllSprites() {
  const sprites = {};
  for (const type of Object.keys(SPRITE_MAPS)) {
    sprites[type] = renderSprite(type);
  }
  return sprites;
}

